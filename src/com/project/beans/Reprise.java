package com.project.beans;

public class Reprise {
	private int id;
	private String Description ;
	private int TauxDépriecié;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getDescription() {
		return Description;
	}
	public void setDescription(String description) {
		Description = description;
	}
	public int getTauxDépriecié() {
		return TauxDépriecié;
	}
	public void setTauxDépriecié(int tauxDépriecié) {
		TauxDépriecié = tauxDépriecié;
	}
	
	
	
}
